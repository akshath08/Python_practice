
# Remove items from Set

completed_anime = {"One Piece", "Naruto", "Bleach", "Death Note", "Black Clover", "Food Wars", "Haikyuu"}

print(completed_anime)

completed_anime.remove("Food Wars")

print(completed_anime)

completed_anime.discard("Hunter X Hunter")

print(completed_anime)


completed_anime.pop()

print(completed_anime)

completed_anime.clear()

print(completed_anime)
